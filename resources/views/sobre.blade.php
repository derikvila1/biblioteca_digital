            <h2>Breve histórico</h2>
            <p>A Biblioteca da Amazônia é o resultado da preocupação do Governo do Estado do Amazonas em resgatar os acervos documentais raros da Biblioteca Pública do Amazonas e do Instituto Geográfico e Histórico do Amazonas. Essas duas instituições mantêm a guarda de acervos que contemplam todas as áreas do conhecimento, e em particular, boa parte das fontes da historiografia do Amazonas. No ano de 2000 foi formulado o primeiro projeto geral de resgate, tratamento e registro em suporte magnético, CD-ROM, tecnologia acessível e disponível na época. Apenas no ano seguinte, foi possível disponibilizar na Internet os acervos. No dia 28 de novembro de 2001 foi lançado a primeira versão do site da Biblioteca, nomeada Biblioteca Virtual do Amazonas, com
              vo de difundir e dar conhecimento das informações valiosas desses documentos para o público e ao mesmo tempo preservar os originais. E desde então, importantes acervos vêm sendo digitalizados e disponibilizados nessa plataforma, como os acervos: do professor Arthur César Ferreira Reis; da coleção de Agnello Bittencourt; do Arquivo Público; de fotografias do Sr. Renato Araújo; do Tribunal de Justiça; de revistas e fotografias da Sra. Denise Cabral; de slides do Sr. Andreas Valentin; de slides do Sr. Leonide Principe; das demais Bibliotecas Públicas, etc. Até o ano de 2013 a administração do site da Biblioteca era de responsabilidade da Diretoria de Gestão da Informação: Núcleo de Documentação Samuel Benchimol, a partir de meados de 2013 passou a ser responsabilidade da Gerência de Acervos Digitais - GAD Em 2019 aconteceu o I Colóquio de Bibliotecas e Informação Digital promovido pela Gerência de Acervos Digitais em parceria com a Gerência da Tecnologia da Informação e a Universidade Federal do Amazonas com o objetivo de debater digital, lançar uma nova versão da Biblioteca Virtual do Amazonas fazendo uso do Sistema Omeka e visando firmar parceria com a Rede Norte de Repositório Institucionais. No entanto, o site ficou inativo e somente o acervo de livros pode ser acessado via uma página no aplicativo da Secretaria, o culturaam. Em novembro de 2020 sob a direção do Departamento de Centros Culturais e Teatro de Difusão, a Gerência de Acervos Digitais ganhou amplitude ao agregar as Bibliotecas Mário Ypiranga e Arthur Reis e se transformar no Centro de Documentação e Memória dos Povos da Amazônia. Neste mesmo mês, a biblioteca virtual foi renomeada para Biblioteca da Amazônia, visto que, a proposta para o novo site não se restringia apenas aos acervos históricos digitais mais também a possibilidade para a disponibilização de diversos outros acervos, programas e eventos e a divulgação de catálogos dos acervos físicos. *Em janeiro de 2022 foi realizado o lançamento do site da Biblioteca da Amazônia pronto e funcional. E diversos projetos estão em andamento para expansão dos acervos digitais e parcerias que agreguem valor ao Centro de Documentação e ao site da Biblioteca.</p>
            <br>

            <h2>Agradecimentos</h2>
            <p>O projeto para a reformulação do site da Biblioteca da Amazônia foi elaborado a partir da reunião das experiências da equipe do acervo digital ao longo desses anos de trabalho e executado sob a supervisão do Secretário de Cultura, o Sr. Marco Apollo Muniz, e dos secretários Sr. Candido e Sr. Kaká que contribuíram com várias sugestões que trazem melhorias de acesso e conteúdo e abriram as portas para que o projeto pudesse acontecer. A coordenação do projeto foi realizada pela Diretora Jessilda Furtado deu energia e direção para a coordenação dos trabalhos pelos Bibliotecários Samuel Souza, Adaísa Melo e Beatriz Alves e equipe. O site é construção da parceria com os diversos departamentos da Secretaria. Gratidão a todos que contribuíram para essa reformulação.</p>

            <br>
            <h2>Acervos</h2>
            <p>E composto pelas obras existentes nas Bibliotecas Públicas. Com o passar dos anos, outros acervos foram adicionados na coleção digital como as bibliotecas particulares de personalidades do Amazonas, documentos históricos de Instituições públicas que fornecem informações relevantes para a construção da memória do Amazonas e produções acadêmicas, cientificas e de cunho em geral que utilizaram o acervo digital na sua composição. As tipologias das obras digitalizadas são: documentos oficiais da provincia, folhetos, fotografias, livros, manuscritos, jornais, desenhos, mapas, microfilmes, plantas e revistas. Quanto ao seu conteúdo, os acervos têm fundo histórico e a maioria dos documentos são voltados para História do Amazonas e da Região Norte do Brasil.</p>
              
            <br>
            <h2>Serviços</h2>
            <p>O projeto para a reformulação do site da Biblioteca da Amazônia foi elaborado a partir da reunião das experiências da equipe do acervo digital ao longo desses anos de trabalho e executado sob a supervisão do Secretário de Cultura, o Sr. Marco Apollo Muniz, e dos secretários Sr. Candido e Sr. Kaká que contribuíram com várias sugestões que trazem melhorias de acesso e conteúdo e abriram as portas para que o projeto pudesse acontecer. A coordenação do projeto foi realizada pela Diretora Jessilda Furtado deu energia e direção para a coordenação dos trabalhos pelos Bibliotecários Samuel Souza, Adaísa Melo e Beatriz Alves e equipe. O site é construção da parceria com os diversos departamentos da Secretaria. Gratidão a todos que contribuíram para essa reformulação.</p>

            <br>

            <h2>Público alvo</h2>
            <p>O objetivo da Biblioteca da Amazônia é alcançar todos os tipos de públicos, desde os estudantes e professores do ensino fundamental, médio e superior até satisfazer uma simples curiosidade da população em geral sobre os contextos da história do Amazonas. Entretanto, o público que marca presença nas visitações online, nos pedidos por pesquisa, sem nenhum tipo de promoção de marketing ou divulgação nas redes sociais sobre a Biblioteca, são majoritariamente dos estudantes de ensino superior, pósgraduandos em mestrado, doutorado e pós-doutorado e pesquisadores independentes. Com o objetivo de resgatar os demais públicos para o uso do site, espera-se que com o site em pleno funcionamento, posteriormente possam ser desenvolvidos planos de divulgação através de projetos para exposição, apresentação e envolvimento da Biblioteca da Amazônia no meio educacional e social.</p>
            <br>

            <h2>Direitos autorais</h2>
            <p>Todas as imagens ou textos, acessíveis no site, são na maioria de domínio público. As obras que estão protegidos pela Lei de Direitos Autorais (Lei 9.610/98) foram cedidas para publicação mediante a assinatura do Termo de Cessão pelos detentores dos direitos autorais. O objetivo da disponibilização das obras e preservar a memor documentos é apenas para fins pessoais (privado), sendo vetada a sua venda, edição ou cópia não autorizada. Para mais informação consulta a Politica de Direitos Autorais para Obras Digitalizadas da GAD.</p>
            <em>*Caso você usuário do site identifique alguma obra que não esteja de acordo com a Lei dos Direitos Autorais, por favor entrar em contato para remoção imediata da obra.</em>

