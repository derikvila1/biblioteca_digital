            <h2>Acessibilidade</h2>
            <p>O acesso à informação é um direito universal e pensando nisso que o site da Biblioteca da Amazônia tem funcionalidades acessiveis a todos os usuarios, de maneira que não haja barreiras para as pessoas com deficiência.</p>
          <br>
            <p>Segundo a Lei Brasileira de Inclusão, considera-se pessoa com deficiência aquela que tem impedimento de longo prazo de natureza física, mental, intelectual ou sensorial, o qual em interação com uma ou mais barreiras, pode obstruir sua participação plena e efetiva na sociedade em igualdade de condições com as demais pessoas. (LEI No 13.146, DE 6 DE JULHO DE 2015).</p>
          <br>
            <p>O papel da biblioteca como um equipamento público, mesmo que sendo em seu formato digital, além de promover o acesso à cultura e informação deve ser encarado como um espaço importante na democratização de seu acesso e na construção de uma sociedade mais inclusiva.</p>
