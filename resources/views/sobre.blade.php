            </p>
            <br>
            <h2>Sobre o sistema Omeka</h2>
            <p>É um software open source desenvolvido para exibir coleções digitais em bibliotecas e museus. O Omeka é um projeto do Centro de História e Novas Mídias, da George Mason University. O Guia de Usuário do Omeka foi criado em parceria entre a Escola Nacional de Administração Pública (Enap) e o Instituto Brasileiro de Informação em Ciência e tecnologia (Ibict). Para mais informações acesse: https://omeka.org/ - https://www.orex.es/omeka/</p>
            
