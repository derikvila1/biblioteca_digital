      <h1 id="title-1">Sobre Nós</h1>
      <h2>Quem somos?</h2>
      <p>A Biblioteca da Amazônia é a representação do conjunto das obras digitais e analógicas sob a responsabilidade do Centro de Documentação e Memória da Amazônia - CDMAM. Essa é a nova versão da Biblioteca Virtual do Amazonas.
        <br><br> O site contará com três espaços de destaques para os acervos digitais e diversos outras funções visando dar publicidade e acessibilidade para todas as coleções de relevância histórica, seja elas digital ou física dos setores da Secretaria de Cultura.<br><br> O primeiro espaço é voltado para os acervos históricos que são o foco principal da Biblioteca. O recorte temporal para as obras serem consideradas históricas é de 1800 a 1980. Não existe registro de nenhum documento anterior a essas datas no acervo.<br><br> O segundo e o terceiro espaços de destaque são algumas das novidades no site. Diante da demanda para publicações de obras atuais, nessa versão, será permitido o cadastro de obras com publicação a partir de 1981 até o ano corrente, desde que, cedidas pelo autor ou pela pessoa que detém os direitos autorais, mediante assinatura do termo de cessão. E como forma de reconhecer cada pesquisador que contou com ajuda das fontes de informação do acervo digital, será disponibilizado um espaço para publicar a produção acadêmica, científica e textos em geral com a temática amazônica.<br><br> Outros espaços disponibilizados são: a obras por assuntos na Área Temática, a Exposições Virtuais Temáticos, a Homenagem a Personalidades Amazônicos. É também espaço para as Bibliotecas Públicas tornar público e acessível os catálogos dos acervos físicos disponível para consulta presencial.
        Assim, promovendo além dos acervos digitais também o acesso a distância aos títulos das obras disponíveis nessas Bibliotecas.<br><br> Para o funcionamento eficiente e eficaz do site da Biblioteca da Amazônia, o Centro de Documentação e Memória dos Povos da Amazônia trabalha em parceria com diversos setores da Secretaria de Cultura e Economia Criativa, como o Departamento de Gestão de Bibliotecas, o Departamentos de Tecnologia e Desenvolvimento, o Departamento de Comunicação e Marketing. Todos contribuem para a evolução da Biblioteca da Amazônia.<br><br> Assim, podemos dizer que o site da Biblioteca da Amazônia se enquadrar perfeitamente na quinta lei da Biblioteconomia que diz que a Biblioteca é um organismo em crescimento e neste sentido avança e se renova com o objetivo de difundir de forma eficaz o riquíssimo acervo digital de documentos de diversas tipologias, ao mesmo tempo em que promove a preservação dos documentos físicos e resgata a memória cultural do Estado. 
      </p>
